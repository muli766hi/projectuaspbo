    private void cariButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariButtonActionPerformed
        // TODO add your handling code here:
        try {
            Akun akun = Database.getInstance().cariAkun(idTextField.getText());
            if(akun.getUsername()==null){
                namaTextField.setText(akun.getNama());
                nimTextField.setText(akun.getNim());
                statusComboBox.setSelectedItem(akun.getStatus());
                jenisComboBox.setSelectedItem(akun.getJenis());
            } else {
                JOptionPane.showMessageDialog(null,"Data tidak ditemukan!");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cariButtonActionPerformed

