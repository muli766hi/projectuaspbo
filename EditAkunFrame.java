    private boolean validasi(){
        if("".equals(namaTextField.getText())){
            warningLabel.setText("Nama tidak boleh kosong");
        } else if ("".equals(nimTextField.getText())){
            warningLabel.setText("NIM tidak boleh kosong");
        }
        return true;
    }
    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        if(validasi()){
            try{
                Akun akun = new Akun();
                akun.setId(idTextField.getText());
                akun.setNama(namaTextField.getText());
                akun.setNim(nimTextField.getText());
                akun.setStatus(String.valueOf(statusComboBox.getSelectedItem()));
                akun.setJenis(String.valueOf(jenisComboBox.getSelectedItem()));
                if(Database.getInstance().editAkun(akun)){
                    JOptionPane.showMessageDialog(null,"Akun sudah berhasil diubah!");
                    this.akun.loadTableData();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null,"Akun gagal diubah!");
                    this.akun.loadTableData();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
            
    }//GEN-LAST:event_updateButtonActionPerformed

